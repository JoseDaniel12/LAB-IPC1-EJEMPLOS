    private void ingresarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarButtonActionPerformed
        String nombreUsuario = usuarioTextField.getText();
        String contrasenia = contraseniaTextField.getText();
        
        int indiceUsuario = -1;
        int contador = 0;
        for (Usuario u : AppState.usuarios) {
            if (nombreUsuario.equals(u.nombreUsuario) && contrasenia.equals(u.contrasenia)) {
                indiceUsuario = contador;
                break;
            }
            contador++;
        }
        
        if (indiceUsuario == -1) {
            JOptionPane.showMessageDialog(this, "Usuario no registrado.", "Alerta", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        AppState.usuarioLogeado = AppState.usuarios.get(indiceUsuario);
        MenuFrame menuFrame = new MenuFrame();
        menuFrame.setVisible(true);
        dispose();
    }//GEN-LAST:event_ingresarButtonActionPerformed

