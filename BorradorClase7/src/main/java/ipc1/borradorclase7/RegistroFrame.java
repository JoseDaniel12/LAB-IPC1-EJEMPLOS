    private void registrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarButtonActionPerformed
        String nombreUsuario = usuarioTextField.getText();
        for (Usuario u : AppState.usuarios) {
            if (nombreUsuario.equals(u.nombreUsuario)) {
                JOptionPane.showMessageDialog(this, "El nombre de usuario ya existe.", "Alerta", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        String fechaNacimiento = formatoFecha.format(dateChooser.getDate());
        String contrasenia = contraseniaTextField.getText();
        String genero = masculinoRadioButton.isSelected() ? "Masculino": "Femenino";
        
        Usuario usuario = new Usuario(nombreUsuario, contrasenia, fechaNacimiento, genero);
        AppState.usuarios.add(usuario);
        JOptionPane.showMessageDialog(this, "El usuario se ha registrado", "Alerta", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_registrarButtonActionPerformed

